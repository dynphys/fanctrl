int main(void)
{

  /* USER CODE BEGIN 1 */
	uint32_t 	ret = 0;
	char 			buf[DEFAULT_UART_LENGTH];
  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC_Init();
  MX_TIM2_Init();
  MX_TIM21_Init();
  MX_USART2_UART_Init();

  /* USER CODE BEGIN 2 */
	Firmware_Init();
	Param_Restore();
	DustOff();
	Set_Regulation(ON);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	while (1)
  {
		//Every 500ms...
		if(g_timer_1ms>500){
			
			if(g_reg_enable==true){
				g_temp = LM35_Convert();

				PWM_Update(&g_RegCh1, g_temp);
				PWM_Update(&g_RegCh2, g_temp);
				PWM_Update(&g_RegCh3, g_temp);
				PWM_Update(&g_RegCh4, g_temp);
				
				if(g_trace_enable==true) {
					memset(buf,0,sizeof(buf));
					sprintf(buf,"%d\r\n",g_temp);
					UART_Send(buf,strlen(buf));
				}
			}
			
			g_timer_1ms = 0;
		}
		
		if(g_UART_Message_Ready==true){
			memset(buf,0,sizeof(buf));
			ret = UART_Execute();
			
			if(ret>0){
				strcpy(buf,MSG_UART_NOK);
			}
			else{
				strcpy(buf,MSG_UART_OK);
			}
			
			UART_Send(buf, strlen(buf));
			g_UART_Message_Ready=false;
		}
		
		if(g_usrbtn == true){
			if(g_reg_enable==true){
				Set_Regulation(OFF);
			}
			else{
				Set_Regulation(ON);
			}
			g_usrbtn = false;
		}
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

  }
  /* USER CODE END 3 */

}

